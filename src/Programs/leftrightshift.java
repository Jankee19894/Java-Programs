public class leftrightshift 
{  
	public static void main(String[] args)
    {
		int number = 2;
        int Ans = number << 2;
        int Ans2 = number >> 2;
        System.out.println("Left shift operator: " + Ans);
        System.out.println("Right shift operator: " + Ans2);
    }
}     